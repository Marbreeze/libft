#include "libft.h"

void			*ft_memmove(void *dst, const void *src, size_t n)
{
	const char	*s;
	const char	*lasts;
	char		*d;
	char		*lastd;

	d = dst;
	s = src;
	if (d < s)
		while (n--)
			*d++ = *s++;
	else
	{
		lasts = s + (n - 1);
		lastd = d + (n - 1);
		while (n--)
			*lastd-- = *lasts--;
	}
	return (dst);
}
