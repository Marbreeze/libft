#include "libft.h"

int		ft_ismin(int *s)
{
	int i;
	int ans;

	i = 0;
	ans = s[0];
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] < ans)
			ans = s[i];
		i++;
	}
	return (ans);
}
